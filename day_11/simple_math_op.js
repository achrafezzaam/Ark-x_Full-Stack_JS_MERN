#!/usr/bin/env node

const addNumbers = (num1, num2) => {
  return num1 + num2;
};

const subNumbers = (num1, num2) => {
  return num1 - num2;
};

const mul = (num1, num2) => {
  return num1 * num2;
};

const div = (num1, num2) => {
  return num1 / num2;
};

console.log(addNumbers(4,6));
console.log(subNumbers(4,6));
console.log(mul(4,6));
console.log(div(4,6));
